package fundamentos;
public class ConversaoTipoPrimitivoNumerico {
    public static void main(String[] args) {
        double a = 1;
        System.out.println(a);

        float b = (float) 1.1234565;
        System.out.println(b);

        double e = 1.999999;
        int f = (int) e;
        System.out.println(f);
    }
}
