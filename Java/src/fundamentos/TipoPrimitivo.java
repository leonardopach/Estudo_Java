package fundamentos;
public class TipoPrimitivo {
    public static void main(String[] args) {
        // tipos numericos inteiror
        byte anosDeEmpresa = 23;
        short numerosDeVoos = 542;
        int id = 65323;
        long pontosAcumulados = 3_123_845_227L;

        // tipos numericos reais
        float salario = 123.45f;
        double salario2 = 123.45;


        // tipo boleano
        boolean ativo = true;
        boolean ativo2 = false;

        // tipo caractere
        char caractere = 'A';
        char caractere2 = 'a';

        // tipo string
        String nome = "Leonardo";

        System.out.println(anosDeEmpresa);
        System.out.println(numerosDeVoos);
        System.out.println(id);
        System.out.println(pontosAcumulados);
        System.out.println(salario);
        System.out.println(salario2);
        System.out.println(salario2);
        System.out.println(ativo);
        System.out.println(ativo2);
        System.out.println(caractere);
        System.out.println(caractere2);
        System.out.println(nome);

    }
}
